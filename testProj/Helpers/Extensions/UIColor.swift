import UIKit

/// расширение UIColor с набором частных цветов в приложении
extension UIColor {
    
    static var tpLightGray: UIColor {
        return UIColor(red: 248 / 255,
                       green: 248 / 255,
                       blue: 248 / 255,
                       alpha: 1.0)
    }
}
